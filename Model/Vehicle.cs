namespace FlexyBox
{
    class Vehicle
{
    /// <summary>
    /// MaxSpeed set in Km/h.
    /// </summary>
    protected int maxSpeed;

    /// <summary>
    /// Set to true if the vehicle is self-propelled.
    /// </summary>
    protected bool motorized;

    /// <summary>
    /// The amount of wheels for this vehicle.
    /// </summary>
    protected int numberOfWheels; 
}
}